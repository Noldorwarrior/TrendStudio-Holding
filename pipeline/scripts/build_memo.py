def _find_percentile(lhs: dict, value: float) -> int:
    """Грубо оценивает, какому процентилю LHS соответствует указанное значение."""
    # Используем интерполяцию по доступным процентилям
    points = [
        (1, lhs["p1_ebitda"]),
        (5, lhs["p5_ebitda"]),
        (25, lhs["p25_ebitda"]),
        (50, lhs["p50_ebitda"]),
        (75, lhs["p75_ebitda"]),
        (95, lhs["p95_ebitda"]),
        (99, lhs["p99_ebitda"]),
    ]
    # линейная интерполяция
    if value <= points[0][1]:
        return points[0][0]
    if value >= points[-1][1]:
        return points[-1][0]
    for i in range(len(points) - 1):
        p_lo, v_lo = points[i]
        p_hi, v_hi = points[i + 1]
        if v_lo <= value <= v_hi:
            frac = (value - v_lo) / (v_hi - v_lo) if v_hi > v_lo else 0
            return int(round(p_lo + frac * (p_hi - p_lo)))
    return 50


