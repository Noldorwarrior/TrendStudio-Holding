def fmt(v: float, d: int = 0) -> str:
    """Форматирует число в ru-стиле: '2 952,24' млн ₽."""
    if d == 0:
        return f"{int(round(v)):,}".replace(",", " ")
    formatted = f"{v:,.{d}f}".replace(",", "~").replace(".", ",").replace("~", " ")
    return formatted


def pct(v: float, d: int = 2) -> str:
    return f"{v * 100:.{d}f}%".replace(".", ",")


